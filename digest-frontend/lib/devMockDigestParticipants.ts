/** Mulberry32 — стабильные «случайные» значения от seed (одинаковые при одном seed). */
function mulberry32(seed: number) {
  return function next() {
    let t = (seed += 0x6d2b79f5);
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

/** Стабильные URL реальных фото (picsum) для dev — разные seed на участника и кадр. */
function photoUrl(kind: "avatar" | "life" | "work", periodId: number, userKey: string, index: number): string {
  const seed = encodeURIComponent(`${periodId}-${userKey}-${kind}-${index}`);
  if (kind === "avatar") {
    return `https://picsum.photos/seed/${seed}/480/480`;
  }
  if (kind === "life") {
    return `https://picsum.photos/seed/${seed}/640/420`;
  }
  return `https://picsum.photos/seed/${seed}/640/400`;
}

