/**
 * Дополнительные участники для проверки вёрстки в dev.
 * Не используется в production-сборке.
 */
export function buildDevExtraParticipants(periodId: number, count: number): Participant[] {
  const rand = mulberry32(periodId * 9973 + 42);
  const out: Participant[] = [];
  const baseId = -900_000;

  for (let i = 0; i < count; i++) {
    const fi = Math.floor(rand() * FIRST.length);
    const li = Math.floor(rand() * LAST.length);
    const first = FIRST[fi];
    const last = LAST[li];
    const layout = EXTRA_LAYOUTS[i % EXTRA_LAYOUTS.length];
    const uid = baseId - i;
    const userKey = `${uid}`;

    out.push({
      user: {
        id: uid,
        first_name: first,
        last_name: last,
        team_id: 1,
        avatar_url: layout.avatar ? photoUrl("avatar", periodId, userKey, 0) : null,
      },
      lifestyle: {
        general_text: LIFESTYLE_COPY[i % LIFESTYLE_COPY.length],
        photos: Array.from({ length: layout.life }, (_, j) => ({
          url: photoUrl("life", periodId, userKey, j),
        })),
      },
      work: {
        general_text: WORK_COPY[i % WORK_COPY.length],
        photos: Array.from({ length: layout.work }, (_, j) => ({
          url: photoUrl("work", periodId, userKey, j),
        })),
      },
      submitted_at: new Date().toISOString(),
    });
  }

  return out;
}
